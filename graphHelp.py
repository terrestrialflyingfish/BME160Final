def charge(aa,pH=7):
    infoDict = {
        'A': (2.34,9.69,0,6.00),  'G': (2.34,9.60,0,5.97),  'M': (2.28,9.21,0,5.74), 'S': (2.21,9.15,0,5.68), 'C': (1.96,8.18,0,5.07),
        'H': (1.82,9.17,6.00,7.59), 'N': (2.02,8.80,0,5.41), 'T': (2.09,9.10,0,5.60), 'D': (1.88,9.60,3.65,2.77), 'I': (2.36,9.60,0,6.02),
        'P': (1.99,10.60,0,6.30), 'V': (2.32,9.62,0,5.96), 'E': (2.19,9.67,4.25,3.22), 'K': (2.18,8.95,10.53,9.74), 'Q': (2.17,9.13,0,5.65),
        'W': (2.83,9.39,0,5.89),  'F': (1.83,9.13,0,5.48), 'L': (2.36,9.60,0,5.98), 'R': (2.17,9.04,12.48,10.76), 'Y': (2.20,9.11,0,5.66)
    }
    t = infoDict[aa]
    coo = -1/(1+(10**(t[0]-pH)))
    nh = 1/(1+(10**(pH-t[1])))
    sc = 0 if t[2] == 0 else -1/(1+(10**(t[2]-pH))) if t[2] < 7 else 1/(1+(10**(pH-t[2])))
    return coo+nh+sc

def phs(seq, pH=7,l=1):
    charges = [charge(aa,pH=pH) for aa in seq]
    k=[]
    for x in range(0,len(seq),l):
        frag = charges[x:x+l]
        k.append(sum(frag)/len(frag))
    return k,pH,l